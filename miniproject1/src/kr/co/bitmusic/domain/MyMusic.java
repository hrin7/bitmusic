package kr.co.bitmusic.domain;

public class MyMusic {
	private int no;
	private String id;


	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String toString() {
		return "MyMusic [no=" + no + ", id=" + id + "]";
	}

}
